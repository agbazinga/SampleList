    private static final String ARG_POSITION = "position";
    private static final String ARG_PARAM2 = "param2";

    private TextView mTextView;
    private FrameLayout mParentLayout;
    private int mParamPosition;

    private OnFragmentInteractionListener mListener;

    public FragmentOne() {
        // Required empty public constructor
    }

    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @param position Parameter 1.
     * @return A new instance of fragment FragmentOne.
     */
    public static FragmentOne newInstance(int position) {
        FragmentOne fragment = new FragmentOne();
        Bundle args = new Bundle();
        args.putInt(ARG_POSITION, position);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParamPosition = getArguments().getInt(ARG_POSITION, 0);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        mParentLayout = (FrameLayout) inflater.inflate(R.layout.fragment_one, container, false);
        mTextView = (TextView) mParentLayout.findViewById(R.id.fragment_one_text_view);
        setupScreens(mParamPosition);
        return mParentLayout;
    }

    private void setupScreens(int position) {
        if (position == 0) {
            mParentLayout.setBackgroundColor(getResources().getColor(R.color.fragment_one_bg_color));
            mTextView.setText("Slide One");
        } else if (position == 1) {
            mParentLayout.setBackgroundColor(getResources().getColor(R.color.fragment_two_bg_color));
            mTextView.setText("Slide Two");
        } else if (position == 2) {
            mParentLayout.setBackgroundColor(getResources().getColor(R.color.fragment_three_bg_color));
            mTextView.setText("Slide Three");
        }
    }

    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(Uri uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

    @Override
    public void onDestroyView() {
        super.onDestroyView();
    }


